    public void mostraInventario(){
        InventarioDAO inventarioDAO = new InventarioDAO();
        
        DefaultTableModel modelo = (DefaultTableModel) Table2.getModel();
        modelo.setNumRows(0);
        
        ArrayList<InventarioDTO> listaDeInventario = inventarioDAO.listarInventario();
        for (int n = 0; n < listaDeInventario.size(); n++){
            modelo.addRow(new Object[]{
                listaDeInventario.get(n).getId(),
                listaDeInventario.get(n).getNome(),
                listaDeInventario.get(n).getData(),
                listaDeInventario.get(n).getQuantidade(),
                listaDeInventario.get(n).getValor()
            });
        }
    }

    public void deletar(){
            DefaultTableModel model = (DefaultTableModel) Table2.getModel();
            try{
                int SelectRowIndex = Table2.getSelectedRow();
                model.removeRow(SelectRowIndex);
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null,"cÃ³digo provisorio");
            }
    }

